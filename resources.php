        <h2>Formatting</h2>
                We get many questions about formatting -- we aren't as strict about formatting as your high school classes were (or your English classes likely are), but we do have some requirements to make grading easier for the TAs:
                        <ul>
                        <li>Have every numbered problem start on its own page (so TAs can easily find where your answers start) </li>
        <li>We don't care if you restate the question or not, but do list the number and letter</li>
        <li>list your collaborators somewhere on the pdf that we can find (we don't have a specific requirement on where -- the first or last page are common choices)</li>
        <li>When you upload to gradescope, make sure you select pages and that the pages are right-side-up</li>
        <li> Putting your name on your submitted pdf is not required (Gradescope automatically associates your name to your submission). We prefer you don't put your own name on your submission (as it reduces the chances of unconscious bias for the graders), but it is not forbidden.</li>
        </ul>
        
